package DTO;

public class Order_DTO {
	private int no;
	private int shsize;
	private int shprice;
	private int shcnt;

	public int getno() {
		return no;
	}

	public void setno(int shno) {
		this.no = shno;
	}

	public int getShsize() {
		return shsize;
	}

	public void setShsize(int shsize) {
		this.shsize = shsize;
	}

	public int getShprice() {
		return shprice;
	}

	public void setShprice(int shprice) {
		this.shprice = shprice;
	}

	public int getShcnt() {
		return shcnt;
	}

	public void setShcnt(int shcnt) {
		this.shcnt = shcnt;
	}

}
